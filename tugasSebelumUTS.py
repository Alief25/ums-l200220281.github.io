def proses_kuliah():
    print("=== Proses Kuliah Mahasiswa ===\n")
    
    # Langkah 1: Pembayaran SPP
    print("Langkah 1: Pembayaran SPP")
    print("Mahasiswa harus membayar SPP terlebih dahulu untuk dapat mengikuti perkuliahan.")
    print("Setelah pembayaran SPP dilakukan, mahasiswa akan mendapatkan akses untuk mengambil mata kuliah.\n")
    
    # Langkah 2: Mengambil Mata Kuliah
    print("Langkah 2: Mengambil Mata Kuliah")
    print("Setelah SPP dibayar, mahasiswa dapat memilih mata kuliah yang ingin diambil.")
    print("Mahasiswa akan mendaftar untuk mata kuliah yang diinginkan dan mendapatkan konfirmasi pendaftaran.\n")
    
    # Langkah 3: Mengikuti Perkuliahan
    print("Langkah 3: Mengikuti Perkuliahan")
    print("Mahasiswa mengikuti perkuliahan sesuai dengan jadwal yang telah ditentukan.")
    print("Selama perkuliahan, mahasiswa akan mendapatkan materi dan tugas dari dosen.\n")
    
    # Langkah 4: Menginput Nilai
    print("Langkah 4: Menginput Nilai")
    print("Setelah menyelesaikan mata kuliah, dosen akan memberikan nilai kepada mahasiswa.")
    print("Mahasiswa akan menginput nilai yang diterima untuk setiap mata kuliah yang diambil.\n")
    
    # Langkah 5: Mendapatkan Nilai Akhir
    print("Langkah 5: Mendapatkan Nilai Akhir")
    print("Setelah semua nilai diinput, mahasiswa dapat melihat nilai akhir dari mata kuliah yang diambil.")
    print("Nilai akhir ini akan menjadi bagian dari transkrip akademik mahasiswa.\n")
    
    print("Proses kuliah telah selesai.")

# Menjalankan fungsi proses_kuliah
proses_kuliah()